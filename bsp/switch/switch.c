/**
 * @brief      读n个拨码开关状态
 * @param  io  GPIO
 * @param  n   GPIO数量
 * @return     拨码值
 */
uint8_t Switch_Read(IO_TypeDef *io, uint8_t n)
{
    uint8_t value;

    value = IO_Read(io, n);
    DEF_BIT_TOGGLE(value, (1u << n) - 1);

    return value;
}

